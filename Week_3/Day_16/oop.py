from turtle import Turtle, Screen

# Turtle() = Class
# timmy = Object
timmy = Turtle()
timmy.shape("turtle")
timmy.color("red")
timmy.forward(100)

# Screen() = Class
# my_screen = Object
my_screen = Screen()

# my_screen.canvheight = attribute (attributes has, variables)
print(my_screen.canvheight)

# my_screen.exitonclick() = method (method does, function)
my_screen.exitonclick()
